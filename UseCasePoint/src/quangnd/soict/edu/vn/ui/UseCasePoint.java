    private void getListJTable(JTable jTable, ResultSet resultSet) throws SQLException{
    while ((jTable.getRowCount() > 0) && (jTable.getRowCount() > 0)) {
            ((DefaultTableModel) jTable.getModel()).removeRow(0);
        }
        int col = resultSet.getMetaData().getColumnCount();
        while (resultSet.next()) {
            Object[] rows = new Object[col];
            for (int i = 1; i <= col; i++) {
                rows[i - 1] = resultSet.getObject(i);
            }
            ((DefaultTableModel) jTable.getModel()).insertRow(resultSet.getRow() - 1, rows);
        }
    }
